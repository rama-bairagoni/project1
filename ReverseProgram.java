package javaprograms;

public class ReverseProgram {

	public static void main(String[] args)
	{
		String input ="hello";
		char c1=input.charAt(4);
		char c2=input.charAt(3);
		char c3=input.charAt(2);
		char c4=input.charAt(1);
		char c5=input.charAt(0);
		
		System.out.print(c1);
		System.out.print(c2);
		System.out.print(c3);
		System.out.print(c4);
		System.out.print(c5);
		
	}

}
